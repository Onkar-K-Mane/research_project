# ------------------------------------------------------------
# OBFUSCATION
# ------------------------------------------------------------
def obfuscate(cmd):
    r = random.random()
    if r < 0.4:  # Base64
        return "powershell -EncodedCommand " + b64encode(cmd.encode('utf-16-le')).decode()
    if r < 0.7:  # Variable split
        v = ''.join(random.choices(string.ascii_lowercase, k=6))
        return f"${v}='{cmd}';IEX ${v}"
    if r < 0.9:  # Concat
        parts = [cmd[i:i+25] for i in range(0, len(cmd), 25)]
        return "IEX ('" + "'+'".join(parts) + "')"
    return cmd  # 10% raw

# ------------------------------------------------------------
# BUILD GRAPH + ROW
# ------------------------------------------------------------
def add(pid, cmd, malicious):
    ts = (datetime.now(timezone.utc) + timedelta(seconds=len(rows))).isoformat() + "Z"
    guid_ = guid()
    parent = guid()
    G.add_node(guid_, type="process", name="powershell.exe", pid=pid, command_line=cmd, first_seen=ts)
    G.add_node(parent, type="process", name="cmd.exe", pid=pid-200)
    G.add_edge(parent, guid_, action="ProcessCreate", timestamp=ts)

    net = dns = files = children = 0
    if malicious:
        net = random.randint(1, 5)
        dns = random.randint(0, 3)
        files = random.randint(1, 4)
        children = random.randint(1, 6)
        for _ in range(net): G.add_edge(guid_, f"ip:185.117.{random.randint(1,254)}.{random.randint(1,254)}", action="NetworkConnect")
        for _ in range(dns): G.add_edge(guid_, f"domain:c2{random.randint(1,99)}.com", action="DnsQuery")
        for _ in range(files): G.add_edge(guid_, f"C:\\Temp\\mal{random.randint(1,999)}.tmp", action="FileCreate")

    cmd_l = cmd.lower()
    rows.append({
        "guid": guid_,
        "pid": pid,
        "command_line": cmd[:500],
        "parent_count": 1,
        "child_process_count": children,
        "network_connections": net,
        "dns_queries": dns,
        "files_created": files,
        "has_invoke": any(x in cmd_l for x in ["iex","invoke"]),
        "has_download": any(x in cmd_l for x in ["download","webclient","iwr"]),
        "has_encoded": any(x in cmd_l for x in ["-enc","-e "]),
        "entropy": round(len(set(cmd))/len(cmd), 3) if cmd else 0,
        "first_seen": ts,
        "label": int(malicious),
    })

